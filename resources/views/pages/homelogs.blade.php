        <h1 class="historytitleheading">A little visit to history lane...</h1>
    </div>
    <div class="historycontainer w-container">
        <p>
            <span class="txthistory">The history of French cuisine dates back to the middle ages. During this time French meals where very
                similar to Moorish Cuisine, and were served in a style called service en confusion, meaning that meals were served all at once.
                Meals consisted of spiced meats such as pork, beef, poultry, and fish. In many cases meals where determined by the season, and
                of what food was in abundance. Meats were salted and smoked to preserve, and vegetables were also salted and put in jars to
                preserve for the winter months. During this time the presentation of the meal was also very important. The more lavish and
                colorful the display, the better, and cooks would use edible items such as saffron, egg yolk, spinach, and sunflower for color.
                One of the most extravagant dinners of this time was a roast swan or peacock, which was sewn back into its skin and feathers
                to look intact. The feet and beak were gilded with gold to complete the spectacle.
            </span>
        </p>
